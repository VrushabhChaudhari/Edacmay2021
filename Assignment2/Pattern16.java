import java.util.*;
import java.lang.*;
class Pattern16
{
	public static void main(String args[])
	{
		for(int i=1;i<=5;i++)
		{
				int z;
				for(int j=1;j<=i;j++)
				{
					z=6-j;
					System.out.print(z);
					System.out.print(" ");
				}
				System.out.println();
		}
		
	}
	
}